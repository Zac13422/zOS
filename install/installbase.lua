function Download(path)
	libinstall.setStep("Downloading ".._branch..path.."                   ")
	data = http.get("https://raw.githubusercontent.com/Zac13422/zOS/".._branch..path)
	data = data.readAll()
	local file = fs.open(path, "w")
	file.write(data)
	file.close()
     
end
 


      
